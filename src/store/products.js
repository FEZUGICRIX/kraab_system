// Function for automatic assignment of unique IDs
const assignIds = (products) => {
  let currentId = 0;
  Object.keys(products).forEach((key) => {
    products[key] = products[key].map((item) => {
      item.id = currentId++;
      return item;
    });
  });
  return products;
};

